public class ArrayP {
	public static void main(String[] args) {
		String [][] A = new String [10][2]  ;
		for(int i=0;i<A.length;i++){
			System.out.println(A[i].length);
			for(int j=0;j<A[i].length;j++)
			System.out.println(A[i][j]);
		}
		
		
		
		
		
	}

}
